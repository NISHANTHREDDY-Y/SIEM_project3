def _send_message(subject, html_body):
    if not EMAIL_ENABLED:
        print("[EMAIL] Email sending is disabled in config.py.")
        return False, "Email disabled"

    if not all([SENDER_EMAIL, SENDER_PASSWORD, RECEIVER_EMAIL]):
        print("[EMAIL] Missing SENDER_EMAIL, SENDER_PASSWORD, or RECEIVER_EMAIL in config.py/environment.")
        return False, "Missing email configuration"

    message = MIMEMultipart("alternative")
    message["From"] = SENDER_EMAIL
    message["To"] = RECEIVER_EMAIL
    message["Subject"] = subject
    message.attach(MIMEText(html_body, "html"))

    try:
        print(f"[EMAIL] Connecting to {SMTP_SERVER}:{SMTP_PORT} with TLS...")
        with smtplib.SMTP(SMTP_SERVER, SMTP_PORT, timeout=20) as server:
            server.ehlo()
            server.starttls()
            server.ehlo()
            print(f"[EMAIL] Authenticating as {SENDER_EMAIL}...")
            server.login(SENDER_EMAIL, SENDER_PASSWORD)
            server.send_message(message)
        print(f"[EMAIL] Email sent successfully to {RECEIVER_EMAIL}.")
        return True, "Email sent successfully"
    except smtplib.SMTPAuthenticationError:
        print("[EMAIL] Authentication failed. Check Gmail address and app password.")
        return False, "Authentication failed: wrong Gmail address or app password"
    except smtplib.SMTPConnectError as exc:
        print(f"[EMAIL] SMTP connection failed: {exc}")
        return False, f"SMTP connection failed: {exc}"
    except (socket.timeout, TimeoutError) as exc:
        print(f"[EMAIL] SMTP timeout: {exc}")
        return False, f"SMTP timeout: {exc}"
    except (smtplib.SMTPException, OSError, socket.gaierror) as exc:
        print(f"[EMAIL] Network/SMTP error: {exc}")
        return False, f"Network/SMTP error: {exc}"
    except Exception as exc:
        print(f"[EMAIL] Unexpected email error: {exc}")
        return False, f"Unexpected email error: {exc}"


def send_email_alert(alert, recommendation=None):
    recommendation = recommendation or {}
    severity = _value(alert, "severity", "INFO")
    threat = _value(alert, "threat", "SIEM Alert")
    alert_id = _value(alert, "id", None)
    subject = f"SIEM {severity} Alert: {threat}"

    if severity not in EMAIL_ALERT_LEVELS:
        print(f"[EMAIL] Skipped {severity} alert. Only MEDIUM, HIGH, and CRITICAL alerts are emailed.")
        return False

    if _value(alert, "email_sent", 0):
        print(f"[EMAIL] Duplicate prevented. Alert {alert_id} was already emailed.")
        return False

    html_body = _build_html_email(alert, recommendation)
    sent, detail = _send_message(subject, html_body)

    if sent and alert_id:
        mark_alert_email_sent(alert_id)

    insert_email_history(
        RECEIVER_EMAIL,
        severity,
        subject,
        "SENT" if sent else "FAILED",
        threat=threat,
        alert_id=alert_id,
        error="" if sent else detail,
    )
    return sent


