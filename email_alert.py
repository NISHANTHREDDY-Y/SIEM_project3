    source = html.escape(_value(alert, "source", "SIEM Dashboard"))
    hostname = html.escape(_value(alert, "hostname") or _value(alert, "computer") or "Unknown")
    device_name = html.escape(_value(alert, "computer") or _value(alert, "hostname") or "Unknown")
